<script>
import TreeChart from "@/components/TreeChart";
import axios from "axios";
import Error from "./Error";
import Store from "../store/index";
import ProData from "../data.js";
import DualPage from "../modals/DualPage";

export default {
  name: "MainTree",
  components: {
    TreeChart,
    Error,
    DualPage
  },
  data() {
    return {
      landscape: [],
      surname: this.$route.params.id,
      cookey: "",
      cookeyStatus: null,
      vloading: false,
      retry: false,
      sess: null,
      newTitle: null,
      dualPageData: {
        showDualPage: false,
        reference: null,
        payload: {}
      }
    };
  },
  computed: {
    loading: {
      get() {
        return Store.state.loading;
      }
    },
    images: {
      get() {
        return Store.state.images;
      }
    },
    tempData: {
      get() {
        return Store.state.tree;
      }
    },
    title: {
      get() {
        return Store.state.title;
      }
    },
    is_session: {
      get() {
        return Store.state.is_session;
      }
    },
    errored: {
      get() {
        return Store.state.error;
      }
    }
  },
  mounted() {
    //called after adding a new member
    this.$root.$on("update-tree", data => {
      console.log(data);
      this.$router.go();
    });
  },
  methods: {
    // Called when a node is clicked
    clickNode: function(node) {
      if (node.data.mate || node.isMate) {
        this.$router.push({
          name: "MemberData",
          params: { member: node.data.id },
          query: { hasMate: true }
        });
      } else {
        this.$router.push({
          name: "MemberData",
          params: { member: node.data.id }
        });
      }
    },

    //handling sessions
    validate() {
      this.vloading = true;
      let sessionUrl = ProData.getHostURL() + "/sessions/";
      let params = {};
      params.pin = this.cookey;
      params.surname = this.surname;
      axios
        .post(sessionUrl, params)
        .then(() => {
          Store.commit("setSession", true);
          this.vloading = false;
        })
        .catch(err => {
          this.retry = true;
          console.log(err);
        })
        .finally(() => {
          this.vloading = false;
        });
    },

    //Dual page calling function
    dualPage(type) {
      this.dualPageData.showDualPage = true;
      this.dualPageData.reference = type;
    },

    dualPageClosed(payload) {
      if (payload) {
        this.newTitle = payload;
      }
      this.dualPageData.showDualPage = false;
    }
  }
};
</script>

